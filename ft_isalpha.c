static int		is_alpha(char c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ? 1 : 0);
}

int		ft_isalpha(char *str)
{
	if (!*str)
		return (1);
	while (*str)
	{
		if (!is_alpha(*str++))
			return (0);
	}
	return (1);
}
