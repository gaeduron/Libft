int		is_numeric(char c)
{
	return ((c >= '0' && c <= '9') ? 1 : 0);
}

int		ft_str_is_numeric(char *str)
{
	if (!*str)
		return (1);
	while (*str)
	{
		if (!is_numeric(*str++))
			return (0);
	}
	return (1);
}
